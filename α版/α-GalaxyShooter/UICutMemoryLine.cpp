using namespace uiCutMemoryLineNS;
//============================================================================================================================================
// Global Variable
// グローバル変数
//============================================================================================================================================
int UICutMemoryLine::instanceIndex = 0;			//	インスタンスインデックス
LPDIRECT3DTEXTURE9 UICutMemoryLine::texture;	//	テクスチャ
//============================================================================================================================================
// Constructor
// コンストラクタ
//============================================================================================================================================
UICutMemoryLine::UICutMemoryLine(void)
{
	// インスタンスインデックスを加算
	instanceIndex++;

	// 初期化
	renderFlag = false;

	// テクスチャのクリア
	texture = NULL;

	return;
}
//============================================================================================================================================
// Destructor
// デストラクタ
//============================================================================================================================================
UICutMemoryLine::~UICutMemoryLine(void)
{
	// 解放
	release();

	return;
}
//============================================================================================================================================
// initialize
// 初期化
//============================================================================================================================================
HRESULT UICutMemoryLine::initialize(LPDIRECT3DDEVICE9 _device, int _playerIndex, TextureLoader* _textureLoader)
{
	// ディレクトリ設定
	setVisualDirectory();

	// テクスチャ読み込み
	texture = *_textureLoader->getTexture(textureLoaderNS::TEXTURE_NUMBER::UI_CUT_MEMORY_LINE);

	// スプライト初期化
	sprite.initialize
	(
		_device,
		texture,														//	テクスチャ
		spriteNS::CENTER,												//	原点
		WIDTH,															//	横幅
		HEIGHT,															//	高さ
		D3DXVECTOR3														//	座標
		(
			_playerIndex ? POSITION_X_PLAYER_2 : POSITION_X_PLAYER_1,	//	座標 x
			POSITION_Y,													//	座標 y
			0.0f														//	座標 z
		),
		D3DXVECTOR3(0.0f, 0.0f, 0.0f),									//	回転
		D3DCOLOR_RGBA(255, 255, 255, 255)								//	色
	);

	return S_OK;
}
//============================================================================================================================================
// release
// 解放
//============================================================================================================================================
void UICutMemoryLine::release(void)
{
	// スプライトのクリア
	sprite.setTexture(NULL);

	// インスタンスインデックスを減算
	instanceIndex--;

	return;
}
//============================================================================================================================================
// update
// 更新
//============================================================================================================================================
void UICutMemoryLine::update(D3DXVECTOR3 _playerPosition1, D3DXVECTOR3 _playerPosition2)
{
	updateRenderFlag(_playerPosition1, _playerPosition2);

	// 他の処理

	return;
}
//============================================================================================================================================
// updateRenderFlag
// 更新 - 描画フラグ
//============================================================================================================================================
void UICutMemoryLine::updateRenderFlag(D3DXVECTOR3 _playerPosition1, D3DXVECTOR3 _playerPosition2)
{
	// 変数宣言：初期化
	D3DXVECTOR3 vector = (_playerPosition1 - _playerPosition2);
	float length = D3DXVec3Length(&vector);

	if (length > 100.0f) { return; }

	renderFlag = true;

	return;
}
//============================================================================================================================================
// render
// 描画
//============================================================================================================================================
void UICutMemoryLine::render(LPDIRECT3DDEVICE9 _device)
{
	if (!renderFlag) { return; }

	sprite.render(_device);

	return;
}